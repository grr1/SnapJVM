public class AllDoubles{

	public static void main(String[] args){
		double a = 5.5;
		double b = 6.0;
		double one = 1.0;
		double negone = -one;
		double mul = one * a;
		double div = a/one;
		double add = a+b;
		double minus = a-b;
		System.out.println(negone);
		System.out.println( minus);
		System.out.println(add);
		System.out.println(mul);
		System.out.println(div);
	}



}
