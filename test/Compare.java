
class Compare {
	public static void main(String args[]) {
	    int i = 2;
	    int j = 4;

	    
	    if(i == 0){
		System.out.println("i == 0");
	    }else if(i > 0){
		System.out.println("i > 0");
	    }else{
		System.out.println("i < 0");
	    }

	    
	    if(i == j){
		System.out.println("i == j");
	    }else if(i > j){
		System.out.println("i > j");
	    }else{
		System.out.println("i < j");
	    }
	}
}

